function T = voigt_rotation_matrix(R)

    T = zeros(6);

    T(1,1)=R(1,1)^2; T(1,2)=R(1,2)^2; T(1,3)=R(1,3)^2;
    T(1,4)=2*R(1,2)*R(1,3);
    T(1,5)=2*R(1,1)*R(1,3);
    T(1,6)=2*R(1,1)*R(1,2);

    T(2,1)=R(2,1)^2; T(2,2)=R(2,2)^2; T(2,3)=R(2,3)^2;
    T(2,4)=2*R(2,2)*R(2,3);
    T(2,5)=2*R(2,1)*R(2,3);
    T(2,6)=2*R(2,1)*R(2,2);

    T(3,1)=R(3,1)^2; T(3,2)=R(3,2)^2; T(3,3)=R(3,3)^2;
    T(3,4)=2*R(3,2)*R(3,3);
    T(3,5)=2*R(3,1)*R(3,3);
    T(3,6)=2*R(3,1)*R(3,2);

    % Remaining rows identical translation of Python code...
    T(4,1)=R(2,1)*R(3,1);
    T(4,2)=R(2,2)*R(3,2);
    T(4,3)=R(2,3)*R(3,3);
    T(4,4)=R(2,2)*R(3,3)+R(2,3)*R(3,2);
    T(4,5)=R(2,1)*R(3,3)+R(2,3)*R(3,1);
    T(4,6)=R(2,1)*R(3,2)+R(2,2)*R(3,1);

    T(5,1)=R(1,1)*R(3,1);
    T(5,2)=R(1,2)*R(3,2);
    T(5,3)=R(1,3)*R(3,3);
    T(5,4)=R(1,2)*R(3,3)+R(1,3)*R(3,2);
    T(5,5)=R(1,1)*R(3,3)+R(1,3)*R(3,1);
    T(5,6)=R(1,1)*R(3,2)+R(1,2)*R(3,1);

    T(6,1)=R(1,1)*R(2,1);
    T(6,2)=R(1,2)*R(2,2);
    T(6,3)=R(1,3)*R(2,3);
    T(6,4)=R(1,2)*R(2,3)+R(1,3)*R(2,2);
    T(6,5)=R(1,1)*R(2,3)+R(1,3)*R(2,1);
    T(6,6)=R(1,1)*R(2,2)+R(1,2)*R(2,1);
end
